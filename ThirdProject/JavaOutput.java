package ThirdProject;

/*You can add as many println() methods as you want. 
* Note that it will add a new line for each method:
*/
public class JavaOutput {
    public static void main(String[] args) {
        System.out.println("Hello World!");
        System.out.println("I am learning Java.");
        System.out.println("It is awesome!");
        /*
         * There is also a print() method, which is similar to println().
         * 
         * The only difference is that it does not insert a new line at the end of the
         * output:
         */
        System.out.print("Hello World! ");
        System.out.print("I will print on the same line.");
        /* They both can print numbers, but dont use quotes for outputting numbers */
        System.out.println(3);
        System.out.println(358);
        System.out.println(50000);
    }
}

// When working with strings, they must be wrapped in double quotes